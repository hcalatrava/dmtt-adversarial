function truth = gen_truth_attack_scenario(model, settings)
% GEN_TRUTH_ATTACK_SCENARIO Generate ground truth for attack scenarios.
% Real target trajectories are generated here for all attack cases.
% The spoofed track (truth.attack) is:
%   - 'hardswitch': generated here with a hardcoded trajectory
%   - 'stealthy'  : initialized here but overwritten by the MPC in run_mc_stealthy.m

    case_id = settings.p.Results.case_id;

    %% --- Initialise truth variables for real targets
    truth.K          = model.K;
    truth.X          = cell(truth.K, 1);
    truth.N          = zeros(truth.K, 1);
    truth.L          = cell(truth.K, 1);
    truth.track_list = cell(truth.K, 1);
    truth.total_tracks = 0;

    %% --- Initialise truth variables for spoofed track
    truth.attack.K          = model.K;
    truth.attack.X          = cell(truth.K, 1);
    truth.attack.N          = zeros(truth.K, 1);
    truth.attack.L          = cell(truth.K, 1);
    truth.attack.track_list = cell(truth.K, 1);
    truth.attack.total_tracks = 0;

    %% --- Generate real (physical) target trajectories
    % Real target trajectories are designed to satisfy the attack feasibility
    % conditions in Sec. III-A: the victim (target 1) passes through a blind
    % region, enabling label extraction, while the impostor (target 2) 
    % enters the non-compromised region enabling label injection. 
    % These trajectories are scenario-specific; the attack framework 
    % generalises to any geometry satisfying A1-A4 in the paper.
    xstart  = settings.xstart;
    tbirth  = settings.tbirth;
    tdeath  = settings.tdeath;
    nbirths = size(xstart, 2);

    for targetnum = 1:nbirths
        targetstate = xstart(:, targetnum);
        for k = tbirth(targetnum):min(tdeath(targetnum), truth.K)

            if targetnum == 1  % victim target
                if k < 46
                    targetstate(1) = targetstate(1) + 30;
                elseif k < 78
                    targetstate(1) = targetstate(1) + 22 * cos(0.05 * (k-46));
                    targetstate(3) = targetstate(3) - 15 * sin(0.05 * (k-46));
                else
                    targetstate(3) = targetstate(3) - 30;
                end

            elseif targetnum == 2  % impostor target
                if k < 43
                    targetstate(1) = targetstate(1) - 45;
                elseif k < 65
                    targetstate(1) = targetstate(1) - 30 * cos(0.075 * (k-43));
                    targetstate(3) = targetstate(3) + 15 * sin(0.075 * (k-43));
                else
                    targetstate(3) = targetstate(3) + 15;
                end
            end

            truth.X{k}          = [truth.X{k}, targetstate];
            truth.track_list{k} = [truth.track_list{k}, targetnum];
            truth.N(k)          = truth.N(k) + 1;
        end
    end

    %% --- Generate spoofed track trajectory
    if strcmp(case_id, 'stealthy')
        % spoofed trajectory generated by MPC in run_mc_stealthy.m
        % truth.attack is already initialised as empty above

    else
        % hardswitch: hardcoded trajectory mimics victim then copies impostor
        xstart_attack = settings.xstart_attack;
        tbirth_attack = settings.tbirth_attack;
        tdeath_attack = settings.tdeath_attack;

        for attacknum = 1:size(xstart_attack, 2)
            targetstate = xstart_attack(:, attacknum);
            for k = tbirth_attack(attacknum):min(tdeath_attack(attacknum), truth.K)
                if k < 38
                    targetstate(1) = targetstate(1) + 30;
                else
                    if ~isempty(truth.X{k})
                        idx = find(truth.track_list{k} == 2, 1);
                        if ~isempty(idx)
                            targetstate = truth.X{k}(:, idx);
                        end
                    end
                end
                truth.attack.X{k}          = [truth.attack.X{k}, targetstate];
                truth.attack.track_list{k} = [truth.attack.track_list{k}, attacknum + nbirths];
                truth.attack.N(k)          = truth.attack.N(k) + 1;
            end
        end
        truth.attack.total_tracks = size(xstart_attack, 2);
        truth.attack.L            = truth.attack.track_list;
    end

    truth.total_tracks        = nbirths;
    truth.L                   = truth.track_list;

end